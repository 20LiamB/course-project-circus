package application.desktop.ui.utils;

/**
 * Rectangle border type.
 */
public enum RectBorderType {
    /**
     * The border is drawn inside the rectangle bounds.
     */
    Inner,
    /**
     * The border is drawn outside the rectangle bounds.
     */
    Outer,
    /**
     * The border is drawn in the middle of the rectangle bounds.
     */
    Middle
}
